#  Importing turtle module
import turtle as t

#Setting place of pen,pen color and background color
t.bgcolor('midnight blue')
t.pencolor('royal blue')
t.penup()
t.bk(240)
t.right(90)
t.fd(100)
t.left(90)
t.pendown()
t.pensize(10)
t.right(45)
t.fd(200)

#  Making octagon and filling color
t.fillcolor('lime')
t.begin_fill()
for i in range(7):
    t.left(45)
    t.fd(200)
t.end_fill()
t.penup()
t.right(180)
t.fd(200)
t.right(45)
t.fd(200)
t.right(45)
t.fd(308)
t.right(90)
t.fd(239)
t.pendown()
t.fillcolor("yellow")
t.begin_fill()

#  Making circle and filling color
t.pencolor('red')
t.circle(-210)
t.end_fill()
t.penup()
t.right(87)
t.fd(305)
t.left(90)
t.fd(120)

#  Writing A
t.pendown()
t.pencolor('blue')
t.pensize(15)
t.left(159) 
t.fd(300)
t.right(145)
t.fd(300)
t.left(180)
t.fd(150)
t.left(73)
t.fd(90)
t.penup()
t.fd(60)
t.right(90)
t.fd(280)

#  Making crown 

t.fillcolor("gold")
t.pendown()
t.pencolor('gold')
t.begin_fill()
t.fd(100)
t.right(135)
t.fd(80)
t.left(100)
t.fd(80)
t.right(110)
t.fd(80)
t.left(100)
t.fd(80)
t.right(135)
t.fd(100)
t.right(90)
t.fd(200)
t.end_fill()

t.done()